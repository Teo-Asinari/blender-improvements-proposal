def _flush_dabs(s, region):
    if s.view_proj is None:
        return
    wm = bpy.context.window_manager
    radius = float(getattr(wm, "gpu_paint_spike_radius", 50.0))
    hardness = float(getattr(wm, "gpu_paint_spike_hardness", 0.5))
    strength = float(getattr(wm, "gpu_paint_spike_strength", 1.0))
    color = tuple(getattr(wm, "gpu_paint_spike_color", (0.9, 0.2, 0.1)))[:3]
    occlusion = bool(getattr(wm, "gpu_paint_spike_occlusion", True))

    queue = s.dab_queue
    s.dab_queue = []
    now = time.perf_counter()
    if s.first_dab_t is None:
        s.first_dab_t = now

    with s.paint_fb.bind():
        s.paint_fb.viewport_set(0, 0, s.size, s.size)
        gpu.state.blend_set('ALPHA')
        gpu.state.depth_test_set('NONE')
        gpu.state.depth_mask_set(False)
        gpu.state.face_culling_set('NONE')
        sh = s.dab_shader
        sh.bind()
        # Per-flush uniforms (identical for every dab in the batch).
        sh.uniform_float("model_matrix", s.model)
        sh.uniform_float("view_proj_matrix", s.view_proj)
        sh.uniform_float("region_size",
                         (float(region.width), float(region.height)))
        sh.uniform_float("brush_radius_px", radius)
        sh.uniform_float("brush_hardness", hardness)
        sh.uniform_float("depth_epsilon", DEPTH_EPSILON)
        sh.uniform_float("use_occlusion", 1.0 if occlusion else 0.0)
        sh.uniform_sampler("scene_depth_tex", s.depth_color_tex)
        for (x, y, pressure) in queue:
            t0 = time.perf_counter()
            sh.uniform_float("brush_center_px", (float(x), float(y)))
            sh.uniform_float(
                "brush_color",
                (color[0], color[1], color[2],
                 strength * max(0.0, min(1.0, pressure))))
            s.batch_dab.draw(sh)
            s.submit_times.append(time.perf_counter() - t0)
        s.dab_count += len(queue)
        s.last_dab_t = time.perf_counter()


# ---------------------------------------------------------------------------
# Stroke-end readback (GPU side; the Image write happens in the modal op)
# ---------------------------------------------------------------------------


def _finalize_stroke_gpu(s):
    import numpy as np

    s.pending_finalize = False
    size = s.size
    stats = {
        "size": size,
        "dabs": s.dab_count,
        "prepass_ms": s.prepass_ms,
    }
    if s.stroke_t0 is not None:
        stats["stroke_s"] = time.perf_counter() - s.stroke_t0
    if s.submit_times:
        stats["submit_avg_ms"] = (sum(s.submit_times)
                                  / len(s.submit_times)) * 1000.0
        stats["submit_max_ms"] = max(s.submit_times) * 1000.0
        stats["submit_total_ms"] = sum(s.submit_times) * 1000.0
    if (s.first_dab_t is not None and s.last_dab_t is not None
            and s.last_dab_t > s.first_dab_t and s.dab_count > 1):
        stats["dabs_per_s"] = ((s.dab_count - 1)
                               / (s.last_dab_t - s.first_dab_t))

    arr = None
    buf = None
    with s.paint_fb.bind():
        # 1x1 read first: drains every queued dab, separating "GPU
        # actually finished the stroke" from the bulk-transfer cost.
        t0 = time.perf_counter()
        s.paint_fb.read_color(0, 0, 1, 1, 4, 0, 'FLOAT')
        stats["drain_ms"] = (time.perf_counter() - t0) * 1000.0

        # ONE full-size read (0.1.0 did two — fb.read_color AND
        # tex.read, an A/B probe artifact; they measured ~equal, so
        # fb.read_color stays as the production path: it guarantees
        # float32 regardless of the RGBA16F attachment and can target a
        # caller-provided Buffer). Preferred: read straight into a
        # Buffer wrapping pre-allocated numpy memory (probed at session
        # start) — no conversion step at all.
        t0 = time.perf_counter()
        if _read_into_numpy:
            try:
                arr = np.empty((size, size, 4), dtype=np.float32)
                s.paint_fb.read_color(
                    0, 0, size, size, 4, 0, 'FLOAT',
                    data=gpu.types.Buffer('FLOAT', (size, size, 4), arr))
                arr = arr.ravel()
                stats["readback_path"] = "read_into_numpy"
            except Exception:
                arr = None
        if arr is None:
            buf = s.paint_fb.read_color(0, 0, size, size, 4, 0, 'FLOAT')
            stats["readback_path"] = _buffer_numpy_path
        stats["fb_read_ms"] = (time.perf_counter() - t0) * 1000.0

    if DEBUG_COMPARE_READS:
        # 0.1.0 A/B probe: GPUTexture.read() (returns the attachment's
        # own format — half floats for RGBA16F). Costs a second full
        # GPU->CPU transfer; never part of the production path.
        try:
            t0 = time.perf_counter()
            s.paint_tex.read()
            stats["tex_read_ms"] = (time.perf_counter() - t0) * 1000.0
        except Exception:
            stats["tex_read_ms"] = float("nan")

    t0 = time.perf_counter()
    if arr is None:
        arr = buffer_to_numpy(buf, _buffer_numpy_path)
    stats["to_numpy_ms"] = (time.perf_counter() - t0) * 1000.0

    s.pending_pixels = arr
    s.pending_gpu_stats = stats


