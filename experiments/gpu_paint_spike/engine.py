def session_active():
    return _session is not None


def stroke_active():
    return _session is not None and _session.stroke_active


def busy():
    """True while a finalize/readback/pixel-write is still in flight."""
    s = _session
    return s is not None and (s.pending_finalize
                              or s.pending_pixels is not None)


def last_error():
    return _session.error if _session is not None else None


def last_stroke_stats():
    return dict(_last_stroke_stats)


def probe_lines():
    if _session is not None and _session.probe_lines:
        return list(_session.probe_lines)
    return []


def start_session(obj, image, region):
    """Create the paint session for ``obj``/``image``. Pure work happens
    here (geometry soup); ALL gpu work waits for the first draw. Safe
    headless: handler registration failures are quietly ignored."""
    global _session
    if _session is not None:
        stop_session()
    coords, uvs = build_mesh_soup(obj)
    if coords is None:
        return False
    s = _Session(obj.name, image.name,
                 image.size[0], region.as_pointer() if region else 0)
    s.coords = coords
    s.uvs = uvs
    _session = s
    _add_handlers()
    return True


def stop_session():
    global _session
    _remove_handlers()
    if _session is not None:
        # Drop gpu refs; Blender frees them with the last reference.
        _session = None


def begin_stroke(x, y, pressure):
    s = _session
    if s is None or s.error is not None:
        return
    s.stroke_active = True
    s.stroke_t0 = time.perf_counter()
    s.first_dab_t = None
    s.last_dab_t = None
    s.dab_count = 0
    s.submit_times = []
    s.last_px = (x, y)
    s.leftover = 0.0
    s.dab_queue.append((x, y, pressure))


def move_stroke(x, y, pressure, radius_px):
    s = _session
    if s is None or not s.stroke_active or s.error is not None:
        return
    x0, y0 = s.last_px
    positions, s.leftover = interpolate_dabs(
        x0, y0, x, y, dab_spacing(radius_px), s.leftover)
    for px, py, _t in positions:
        s.dab_queue.append((px, py, pressure))
    s.last_px = (x, y)


def end_stroke():
    s = _session
    if s is None or not s.stroke_active:
        return
    s.stroke_active = False
    s.pending_finalize = True   # draw callback runs the GPU readback


def take_pending_pixels():
    """(numpy array, image_name) awaiting the Image write, or None.
    Called from the modal operator (never from a draw callback)."""
    s = _session
    if s is None or s.pending_pixels is None:
        return None
    arr = s.pending_pixels
    s.pending_pixels = None
    return arr, s.image_name


